marks = [35, 67, 94, 100]

# Type conversion from list to string
marks_str = str(marks)

# Print list
print("Marks list: "+marks_str)

# list indexing
print(marks[0])
print(marks[1])
print(marks[2])

# Print number of list in reverse
print("\nList in reverse order: \n")
print(marks[-2])

# Print list range
print("\nRange based list: \n")
print(marks[0:2])